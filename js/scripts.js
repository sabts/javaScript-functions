function sum (numberA, numberB) {
  const result = numberA + numberB;
  return result
}

function division (numberA, numberB) {
  const result = numberA / numberB;
  return result
}

function multiply (numberA, numberB){
  const result = numberA * numberB;
  return result
}

function remainderOfDivision (numberA, numberB) {
  const result =numberA % numberB
  return result
}


